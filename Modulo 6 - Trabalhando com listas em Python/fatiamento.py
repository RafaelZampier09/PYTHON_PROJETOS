letras = list("python")

print(letras[2:]) # Retorna tudo pós 2º indice
print(letras[:2]) # Retorna os dois primeiros indices
print(letras[1:3]) # Retorna os valores do primeiro indice até o terceito (no caso duas letras)
print(letras[0:3:2]) # Retorna os valores do indice 0 até o indice 3 pulando de 2 em 2
print(letras[::]) # Retorna uma copia da lista
print(letras[::-1]) # Retorna os indices invertidos