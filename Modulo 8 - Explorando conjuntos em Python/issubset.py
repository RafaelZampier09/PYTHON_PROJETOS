conjunto_a = {1, 2, 3}
conjunto_b = {1, 2, 3, 4, 5}

print(conjunto_a.issubset(conjunto_b)) # O conjunto A pertence ao conjunto B, pois todos os valores do A estão no B
print(conjunto_b.issubset(conjunto_a)) # O conjunto B não pertence ao conjunto A

# Issubset revela se o conjunto A pertence ao conjunto B ou vice versa