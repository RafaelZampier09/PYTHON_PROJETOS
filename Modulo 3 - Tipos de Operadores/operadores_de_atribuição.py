saldo = 500 # Atribuição simples
print(f"O saldo inicial é de {saldo}")

saldo += 200 # Atribuição com soma
print(f"Com soma de 200, o saldo foi para {saldo}")

saldo -= 200 # Atribuição com subtração
print(f"Com a subtração de 200, o saldo foi para {saldo}")

saldo //= 2 # Atribuição de divisão com numero inteiro
print(f"Com a divisão por 2 com numero inteiro, o saldo foi para {saldo}")

saldo /= 2 # Atribuição com divisão
print(f"Com a divisão por 2, o saldo foi para {saldo}")

saldo *= 5 # Atribuição de multiplicação
print(f"Com a multiplicação por 5, o saldo foi para {saldo}")

saldo **= 2 # Atribuição por exponenciação
print(f"Com a exponenciação por 2, o saldo foi para {saldo}")

saldo %= 3 # Atribuição de modulo
print(f"Com o modulo de 3, o saldo foi para {saldo}")


