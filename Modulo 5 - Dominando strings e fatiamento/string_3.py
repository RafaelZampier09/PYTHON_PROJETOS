nome = "Rafael Zampier"

print(nome[0]) # Exibe apenas o primeiro caractere da variavel.
print(nome[-3::]) # Exibe apenas o ultimo caractere da variavel.
print(nome[:9]) # Exibe do primeiro caractere até o caractere desejado, no caso do exemplo, 9.
print(nome[9:]) # Exibe informações a partir do caractere desejado até o final, no caso definimos o caractere 9 até o fim.
print(nome[3:10]) # Exibe uma sequencia de caractere, no caso ira exibir do caractere de indice 3 ate o de indice 10.
print(nome[3:10:2]) # Quase a mesma coisa que o de cima, com a diferença que ele ira pular de 2 em 2.
print(nome[:]) # Exibe uma copia da variavel.
print(nome[::-1]) # Exibe a variavel de forma invertida.